        public static string ConvertSoftbankEmojis(string str)
        {
            var fixedStr = "";

            foreach (var c in str)
            {
                var codePoint = ((int)c).ToString("x4");
                if (codePoint[0] == 'e')
                {

                    var theEmoji = EmojiMaps.FirstOrDefault(emoji => emoji.Softbank == codePoint);

                    if (theEmoji == null)
                    {
                        continue;
                    }

                    fixedStr += theEmoji.Utf16;
                }
                else
                {
                    fixedStr += c;
                }
            }

            return fixedStr;
        }

        public static string UnifiedToUtf16(string unified)
        {
            var unifiedLower = unified.ToLower();
            foreach (var emojiMap in EmojiMaps)
            {
                if (emojiMap.Unified == unifiedLower)
                    return emojiMap.Utf16;
            }

            return null;
        }

